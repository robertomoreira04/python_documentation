username = 'Roberto Moreira'

print(username[:]) # todo o nome 

print(username[:2]) # do início ao índice 2

print(username[0:9]) # do inicio ao indice 9, considerando espaços

print(username[::2]) # de 2 em 2 caracteres

print(username[1::2]) # começa do indice 1 e vai de 2 em 2, ou seja, buscando ímpares

print(username[::-1]) # reverte os indices



